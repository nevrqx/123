  function visibleHeads(){
    return Array.from(document.querySelectorAll('.qa-head')).filter(h => h.closest('.question-item') && h.closest('.question-item').style.display !== 'none');
  }
  function focusFirstVisible(){
    const heads = visibleHeads();
    if(heads[0]) heads[0].focus();
  }
  function copyAnswer(contentNode, btn){
    const text = contentNode.textContent.trim();
    if(navigator.clipboard && navigator.clipboard.writeText){
      navigator.clipboard.writeText(text).then(() => {
        const prev = btn.textContent; btn.textContent = 'Скопировано'; btn.disabled = true; setTimeout(() => { btn.textContent = prev; btn.disabled = false; }, 1200);
      });
    }
  }
  document.addEventListener('keydown', (e) => {
    if((e.ctrlKey || e.metaKey) && e.key.toLowerCase() === 'k'){ if(input){ input.focus(); input.select(); e.preventDefault(); } return; }
    if(e.key === '/' && document.activeElement !== input){ if(input){ input.focus(); e.preventDefault(); } return; }
    const active = document.activeElement;
    if(active && active.classList && active.classList.contains('qa-head')){
      const heads = visibleHeads();
      const i = heads.indexOf(active);
      if(e.key === 'ArrowDown' && i < heads.length - 1){ heads[i+1].focus(); e.preventDefault(); }
      else if(e.key === 'ArrowUp' && i > 0){ heads[i-1].focus(); e.preventDefault(); }
      else if(e.key === 'Home'){ if(heads[0]) heads[0].focus(); e.preventDefault(); }
      else if(e.key === 'End'){ if(heads[heads.length-1]) heads[heads.length-1].focus(); e.preventDefault(); }
      else if(e.key === 'Enter' || e.key === ' '){ const it = active.closest('.question-item'); setOpen(it, !it.classList.contains('open')); e.preventDefault(); }
    }
  });

